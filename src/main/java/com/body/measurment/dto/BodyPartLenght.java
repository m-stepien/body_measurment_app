package com.body.measurment.dto;

public class BodyPartLenght {
    private Double arm;
    private Double head;
    private Double neck;
    private Double chest;
    private Double thigh;
    private Double shin;
    private Double forarm;
    private Double belly;

    public BodyPartLenght() {
    }

    public Double getArm() {
        return arm;
    }

    public void setArm(Double arm) {
        this.arm = arm;
    }

    public Double getHead() {
        return head;
    }

    public void setHead(Double head) {
        this.head = head;
    }

    public Double getNeck() {
        return neck;
    }

    public void setNeck(Double neck) {
        this.neck = neck;
    }

    public Double getChest() {
        return chest;
    }

    public void setChest(Double chest) {
        this.chest = chest;
    }

    public Double getThigh() {
        return thigh;
    }

    public void setThigh(Double thigh) {
        this.thigh = thigh;
    }

    public Double getShin() {
        return shin;
    }

    public void setShin(Double shin) {
        this.shin = shin;
    }

    public Double getForarm() {
        return forarm;
    }

    public void setForarm(Double forarm) {
        this.forarm = forarm;
    }

    public Double getBelly() {
        return belly;
    }

    public void setBelly(Double belly) {
        this.belly = belly;
    }
}
